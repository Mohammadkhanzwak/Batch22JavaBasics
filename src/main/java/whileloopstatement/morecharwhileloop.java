package whileloopstatement;

public class morecharwhileloop {
    public static void main(String[] args) {

        char learn= 'A';

        while(learn <='Z'){
            System.out.println(learn);
            learn++;
            //for loop exercise

            for(char score= 'A';score<'Z'; score++){
                System.out.println(score +"");
            }
        }
    }
}
