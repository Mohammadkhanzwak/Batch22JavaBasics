public class Relationaloperator {
    public static void main(String[] args) {

        int num1=22;
        int num2=14;

        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1==num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);

        boolean result=num1<=num2;
        result=num1<=num2;
        System.out.println(result);

        result=num1>=num2;
        System.out.println(result);
        System.out.println(num1=num2);



    }
}
