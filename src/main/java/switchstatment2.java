public class switchstatment2 {
    public static void main(String[] args) {

        String school="majic";

        switch(school){

            case"math":
                System.out.println("i wanna learn math");
                break;

            case"chemistry":
                System.out.println("i wanna learn chemistry");
                break;

            case"geographic":
                System.out.println("geographic");
                break;

            case"sport":
                System.out.println("i wanna play sport");
                break;

            case"history":
                System.out.println("i wanna learn history");
                break;

            case"science":
                System.out.println("i am good at science");
            default:
                System.out.println("invalid input");
        }
    }
}
